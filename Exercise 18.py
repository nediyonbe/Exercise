# Enter your code here. Read input from STDIN. Print output to STDOUT
N = int(input().split()[0])
# print(N)
M = N * 3
r = '.|.'

for i in range(int((N - 1)/2)):
    print((r*(2*i+1)).center(M,'-'))

print('WELCOME'.center(M, '-'))

for i in reversed(range(int((N - 1)/2))):
    print((r*(2*i+1)).center(M,'-'))

